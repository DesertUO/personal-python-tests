# Dado 3 notas, escribir un programa que determine el promedio de un alumno

# Leer los datos (las 3 notas del alumno)
Nota1 = float(input("Ingrese la primera nota del alumno: "))
Nota2 = float(input("Ingrese la segunda nota del alumno: "))
Nota3 = float(input("Ingrese la tercera nota del alumno: "))
# Calcular el promedio del alumno
Promedio = (Nota1 + Nota2 + Nota3) / 3
# Mostrar los resultados (el promedio del alumno)
print("El promedio del alumno es", Promedio)
